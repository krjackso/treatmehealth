import UIKit

extension UIColor {

    static func offWhite() -> UIColor {
        return UIColor(colorLiteralRed: 250/255.0, green: 250/255.0, blue: 250/255.0, alpha: 1)
    }

    static func TMLightGray() -> UIColor {
        return UIColor(colorLiteralRed: 220/255.0, green: 220/255.0, blue: 220/255.0, alpha: 1)
    }

    static func TMPurple() -> UIColor {
        return UIColor(colorLiteralRed: 84/255.0, green: 13/255.0, blue: 110/255.0, alpha: 1)
    }

    static func TMRed() -> UIColor {
        return UIColor(colorLiteralRed: 231/255.0, green: 29/255.0, blue: 54/255.0, alpha: 1)
    }

    static func TMOrange() -> UIColor {
        return UIColor(colorLiteralRed: 255/255.0, green: 119/255.0, blue: 51/255.0, alpha: 1)
    }

    static func TMGreen() -> UIColor {
        return UIColor(colorLiteralRed: 129/255.0, green: 193/255.0, blue: 75/255.0, alpha: 1)
    }

    static func TMBlue() -> UIColor {
        return UIColor(colorLiteralRed: 6/255.0, green: 123/255.0, blue: 194/255.0, alpha: 1)
    }

}